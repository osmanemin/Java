    public void listeYap(){
        
        Map<String,Integer> list = new LinkedHashMap<String,Integer>();
        
        list.put("Bilgisayar", 3500);
        list.put("Bulaşık Makinesi", 2200);
        list.put("Yapay Zekalı Robot", 6499);
        
        DefaultTableModel model = (DefaultTableModel)AlımSatımTablosu.getModel();
        
        for(Map.Entry<String,Integer> entry : list.entrySet()){
            String a = entry.getKey();
            String b = entry.getValue().toString();
            Object[] eklenecek = {a,b};
            model.addRow(eklenecek);
        }
        
        
     }

