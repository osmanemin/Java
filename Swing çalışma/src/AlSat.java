    private void Al_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Al_buttonActionPerformed

        
        DefaultTableModel model = (DefaultTableModel)AlımSatımTablosu.getModel();
        int secili_row = AlımSatımTablosu.getSelectedRow();
        String ücret = (String) model.getValueAt(secili_row, 1);
        int ücrett = Integer.parseInt(ücret);
        if (bakiye - ücrett < 0){
            JOptionPane.showConfirmDialog(this, "YETERSİZ BAKİYE!!!");
            return;
        }
        bakiye -= ücrett;
        bakiyeeee.setText("Bakiye "+ bakiye);
        
        
    }//GEN-LAST:event_Al_buttonActionPerformed

